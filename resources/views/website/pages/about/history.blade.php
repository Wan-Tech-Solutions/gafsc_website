                            <p>
                                The idea to establish a Staff College for Ghana was conceived immediately on the attainment 
                                of independence from Great Britain in 1957, when it was realised that it was necessary to 
                                train Ghanaian officers to take over from the departing British officers serving with the Ghana Army.  
                                The idea was however shelved because of logistics and other constraints at the time. Ghana therefore 
                                continued to depend on the vacancies offered at the Staff Colleges of Commonwealth and other allied 
                                countries.
                            </p>

                            <p>
                                In the early 1960s, as the Ghana Armed Forces expanded, the requirement for more trained Staff 
                                Officers increased considerably.  Consequently, the National War College (NWC) was established 
                                in October 1963 to train selected officers for Intermediate Command and Staff Appointments.
                            </p>

                            <p>
                                In December 1963, the National War College was renamed the National Defence College (NDC).  
                                The aim of the College remained unchanged, but the duration of the Course was increased from 
                                10 weeks to 21 weeks.  In January 1964, the institution was once again renamed the Junior Defence 
                                College (JDC), and in 1973, the Junior Defence College was opened to students from allied African 
                                countries.
                            </p>

                            <p>
                                In early 1976, almost 20 years after the idea was conceived, the Ghana Government made a firm decision 
                                and commitment to establish a Grade II Staff College as the highest institution of learning where midstream
                                 officers of the rank of Maj and Lt Col and equivalents in the Navy and Air Force would be trained.  The United 
                                 Kingdom and Canada supported the project by providing a Military Advisory Team made up of 6 officers from the 
                                 United Kingdom and 3 from Canada.
                            </p>

                            <p>
                                With this, the Ghana Armed Forces Staff College (GAFSC) was established in July 1976 as a Tri-Service and Joint 
                                Institution.  The UK – Canada Military Advisory Team worked jointly with selected Ghanaian Officers to form the 
                                nucleus of the faculty of the newly established GAFSC.  In October of the same year, the Junior Defence College 
                                was placed under the Command of the GAFSC as its Junior Division, and the Course run there was changed to the Junior 
                                Staff Course (JSC).
                            </p>

                            <p>
                                On 10 January 1977, Course 1 of the Senior Division of the Ghana Armed Forces Command and Staff College (GAFCSC) 
                                assembled with 28 Ghanaian Officers.  Their successful completion of the course and graduation on 16 December 1977, 
                                demonstrated the capability of the Ghana Armed Forces to shoulder the responsibility of training selected officers 
                                for higher Command and Staff appointments to meet international standards and our national needs.
                            </p>

                            <p>
                                Over the years, the College has received and trained personnel from sister African nations and has concentrated on 
                                core military/defence programmes, leading to the award of Pass Staff College (PSC) certificate.The need for venturing 
                                into other programmes that would arm graduates of GAFCSC with the requisite knowledge was necessitated by imperatives 
                                of the global system. The Ghana Armed Forces (GAF) has participated in peace support operations since 1960. It is this 
                                imperative that informed the decision to widen the scope of courses run by  GAFCSC. The College therefore sought 
                                collaboration with the University of Ghana and GIMPA to run various courses to satisfy the felt need already mentioned
                                above. Having obtained Institutional Accreditation, the College is now poised to run its own courses, while not truncating 
                                the collaborative link with the aforementioned institutions.

                            </p>

                            <p>
                                Towards the realization of its new focus and aims, the College embarked upon massive infrastructural expansion. A new 
                                complex to house the College has been completed. It has all the facilities needed for running the programmes envisaged 
                                including classrooms, administrative offices, and auditorium, audio-visual aid systems, a library (including a laboratory) 
                                etc.
                            </p>

                            <p>
                                GAFCSC is noted, as a world-class College and a regional training centre, not only in Defence and Military Studies, 
                                but also in Governance and Leadership, International Politics, Administration and Management, as well as Crisis and 
                                Conflict Management. In this, the College has collaborated with both local and international institutions of higher 
                                learning – University of Ghana, Legon, Ghana Institute of Management and Public Administration (GIMPA), 
                                Cranfield University (UK), and Bradford University (UK).
